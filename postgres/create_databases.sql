GRANT ALL PRIVILEGES ON ALL TABLES IN SCHEMA public TO catalogue_app;
GRANT ALL PRIVILEGES ON ALL SEQUENCES IN SCHEMA public TO catalogue_app;
ALTER DEFAULT PRIVILEGES IN SCHEMA public GRANT ALL PRIVILEGES ON TABLES TO catalogue_app;
ALTER DEFAULT PRIVILEGES IN SCHEMA public GRANT ALL PRIVILEGES ON SEQUENCES TO catalogue_app;

