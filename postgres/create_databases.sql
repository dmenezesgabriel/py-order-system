CREATE DATABASE "catalogue";
CREATE DATABASE "order";

CREATE USER catalogue_app WITH PASSWORD '123abc';
CREATE USER order_app WITH PASSWORD '123abc';

GRANT ALL PRIVILEGES ON DATABASE "catalogue" TO catalogue_app;
GRANT ALL PRIVILEGES ON DATABASE "order" TO order_app;

\c catalogue
